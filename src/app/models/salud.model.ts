export class Salud {

    identificador: number;
    coordenadaX: number;
    coordenadaY: number;

    constructor(
        identificador: number,
        coordenadaX: number,
        coordenadaY: number,
    ) {
        this.identificador = identificador;
        this.coordenadaX = coordenadaX;
        this.coordenadaY = coordenadaY;
    }
}